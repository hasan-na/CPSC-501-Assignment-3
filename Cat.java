public class Cat {
    private int age;
    private boolean isThirsty;

    public Cat(int age, boolean isThirsty) {
        this.age = age;
        this.isThirsty = isThirsty;     
    }

    public int getAge()
    {
        return age;
    }
    public boolean getThirst()
    {
        return isThirsty;
    }
}
